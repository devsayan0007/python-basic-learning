# User Input & Type Casting in Python
# Key Point 
# i) input()
# ii) int()
# iii) float()
# iv) eval()

a = input("Enter The Value ")
print(a," ", type(a))

b = int(input("Enter an integer value "))
print(b," ", type(b))

c = float(input("Enter a Float value "))

print(c," ", type(c))

d = eval(input("Enter anything "))

print(d," ", type(d))